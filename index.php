			<h2>1. Le but du jeu</h2> 
		</div>
		<div class="content">
			<h3>Comment se déroule une partie ?</h3>
			<p>	Le jeu se décompose en sessions, avec deux équipes qui s'affrontent par le biais de champions invoqués par les joueurs, lesquels incarnent des invocateurs. Chaque équipe comprenant 3 ou 5 joueurs en fonction du mode de jeu, a pour but de détruire le “nexus”, le batiment principale de l’équipe adverse. Pour cela, chaque équipe devra avancer sur une carte, en détruisant tourelle par tourelle pour avancer à l’aide d’une armée de petit monstre. Il sera également possible de tirer avantage de la carte, notamment dans la “jungle” ou la plupart des monstres nous offrent des bonus et avantages lors de leur mort.
			</p>
			<p>
			Au début de chaque bataille, chacun des joueurs peut choisir un champion qui va le représenter au combat. Il peut choisir parmi plus de cents champions et acheter son équipement parmi une liste de 200 objets uniques pour créer des combinaisons efficaces au combat: objets de base (potions, élixirs ou balises de vision), équipements classiques (caractéristiques ou capacités de combat), et objets légendaires, créés en combinant plusieurs équipements. Une grande partie de ces objets confèrent des effets passifs ou actifs souvent plus complexes qu'une simple augmentation de statistiques (ralentissement des ennemis touchés par un sort, saignement, révélation d'une partie de la carte, etc.). De multiples stratégies sont donc possibles au cours d’une partie.</p>
		</div>
