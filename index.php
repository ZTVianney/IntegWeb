			<h3>League of Legends, c'est quoi ?</h3>
			<p>League of Legends (parfois abrégé LoL) est un jeu vidéo d'ordinateur de type arène de bataille en ligne multijoueur (MOBA) gratuit sorti en 2009 et développé et édité par Riot Games sur Windows et Mac OS X.
			Depuis, League of Legends est devenu rapidement un véritable phénomène. C'est en 2013 qu'il devient l'un des jeux les plus joués au monde. Il séduit aussi le monde du sport électronique en devenant l'événement eSport le plus regardé de l'histoire, avec plus de 32 millions de spectateurs en ligne et plus de 8,5 millions de vues simultanées lors de la finale de la saison 3. Le record est de nouveau battu lors de la saison 5 avec un total de 36 millions de spectateurs avec un pic d'audience de 14 millions de spectateur.</p>
